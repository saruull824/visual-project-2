package application;

public class huuhedProfile {
	private String v_ner;
	private String v_tun;
	private String ognoo;
	private String emch;
	private String suvilagch;
	private String tailbar;
	
	public huuhedProfile(String v_ner, String v_tun, String ognoo, String emch, String suvilagch, String tailbar) {
		super();
		this.v_ner = v_ner;
		this.v_tun = v_tun;
		this.ognoo = ognoo;
		this.emch = emch;
		this.suvilagch = suvilagch;
		this.tailbar = tailbar;
	}

	public String getV_ner() {
		return v_ner;
	}

	public void setV_ner(String v_ner) {
		this.v_ner = v_ner;
	}

	public String getV_tun() {
		return v_tun;
	}

	public void setV_tun(String v_tun) {
		this.v_tun = v_tun;
	}

	public String getOgnoo() {
		return ognoo;
	}

	public void setOgnoo(String ognoo) {
		this.ognoo = ognoo;
	}

	public String getEmch() {
		return emch;
	}

	public void setEmch(String emch) {
		this.emch = emch;
	}

	public String getSuvilagch() {
		return suvilagch;
	}

	public void setSuvilagch(String suvilagch) {
		this.suvilagch = suvilagch;
	}

	public String getTailbar() {
		return tailbar;
	}

	public void setTailbar(String tailbar) {
		this.tailbar = tailbar;
	}
	
}
